package rectangleSquare;

public class Input {
     private int x;
     private int y;
     
     Input (int x,int y){
    	 this.x=x;
    	 this.y=y;
     }
     Input() {
    	 x=0;
    	 y=0;
     }
     
     int getX(){
    	 return x;
    	 
     }
     int getY() {
    	 return y;
     }
     
     void setX(int x) {
    	 this.x=x;
     }
     void setY(int y) {
    	 this.y=y;
     }
     
     
     
     
}
